int		ft_sqrt(int nb)
{
	int nbr;

	nbr = 1;
	if (nb <= 0 || nb > 2147395600)
		return (0);
	while ((nbr * nbr) <= nb)
	{
		if ((nbr * nbr) == nb)
			return (nbr);
		nbr++;
	}
	return (0);
}
