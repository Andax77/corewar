int		ft_atoi(const char *str)
{
	int nb;
	int n;

	nb = 0;
	while (*str == ' ' || *str == '\t' || *str == '\n'
			|| *str == '\v' || *str == '\f' || *str == '\r')
		++str;
	n = (*str == '-') ? -1 : 1;
	if (*str == '-' || *str == '+')
		++str;
	while (*str >= '0' && *str <= '9')
	{
		nb += (*str - '0');
		if (*(++str) >= '0' && *str <= '9')
			nb *= 10;
	}
	return (nb * n);
}
